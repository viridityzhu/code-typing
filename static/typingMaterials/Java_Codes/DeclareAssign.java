public class DeclareAssign{
	public static void main(String args[]){
		boolean b=true;
		int x,y=8;
		float f=4.5f;
		double d=3.1415;
		char c;
		c='\u0031';
		x=12;
		System.out.println("b="+b);
		System.out.println("x="+x);
		System.out.println("y="+y);
		System.out.println("f="+f);
		System.out.println("d="+d);
		System.out.println("c="+c);
	}
}